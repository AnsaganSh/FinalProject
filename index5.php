    <h1 id='text'>Женская одежда</h1>
    <p id='text'>
        Женский традиционный костюм включал платье свободного кроя, сверху носили камзол или халат-шапан. В основном женскую одежду шили из ситца, а знатные казашки могли себе позволить костюмы из шелка и бархата. Также использовался тонкий войлок. Крой платья был закрытым, и лишь в 19 веке в казахской национальной одежде появился отрезной фасон платья, когда юбка и лиф сначала кроились отдельно, а затем сшивались. В холодное время женщины надевали шубы из лисьего меха или шкуры ягненка.

        Красный цвет в одежде разрешалось носить только молодым девушкам, а после тридцати лет это считалось недопустимым. Также, чем старше была женщина, тем меньше на ней было украшений.

        Очень активно использовалась в отделке казахских нарядов вышивка, а также украшение бисером, войлоком, бляхами из серебра, различными нашивками. Головные уборы девушек было принято украшать перьями филина, которые, как считалось, оберегают от злых сил.
