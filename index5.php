        Казахский мужской костюм включал в себя нательную рубаху и штаны, сверху надевался халат (шапан), на голову – шапка (зимой – тымак, а летом – калпак). При этом шапан считался одним из важнейших элементов мужского костюма, показывая статус мужчины. Шапаны могли быть различной толщины и расцветки. Нарядный праздничный шапан отличался от повседневного глубоким цветом и роскошной золотой вышивкой.

