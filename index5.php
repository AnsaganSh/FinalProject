    <h1 id='text'>Головной убор</h1>
    <iframe id='iframe' src="https://www.youtube.com/embed/HQd-WrYLK2Y" title="Казахские национальные головные уборы для девушек." frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
    <p id='text'>

        Головной убор имел сакральный смысл в казахской национальной одежде. Казахи очень трепетно относились к головным уборам, и поэтому с ними связано множество поверий. Так, недопустимо было носить чужую шапку. Также нельзя было ронять головной убор или хранить его в ненадлежащем месте.

        Саукеле – уникальный и самобытный головной убор, который занимает особое место в традиционной одежде казахов. Саукеле носили казахские невесты, и он являлся самым дорогим и красивым элементом свадебного наряда. Саукеле был конусообразной формы, его украшали жемчужинами, кораллами, бирюзой, самоцветами и мелкими монетами. На верхушке саукеле, высота которого составляла около семидесяти сантиметров, красовался пучок перьев филина. Дополнялся саукеле специальными боковыми подвесками «жактау», они были длинными и могли доходить до пояса и ниже. Подвески также украшали многочисленными декоративными элементами. Ещё саукеле дополняли меховыми наушниками и височниками. Сверху головной убор накрывали расшитыми платками, а также прикрепляли к нему вуаль «желек».

        После свадьбы костюм замужней женщины дополнялся расшитой искусными золотыми узорами шапкой «касаба». Как только у женщины рождался первенец, она начинала носить белый тюрбан «кимешек».

        У мужчин было принято носить войлочную шляпу под названием «калпак», а также шапку-тюбетейку «такию». В холодное время мужчины и женщины надевали меховую шапку «борик» или головной убор из овчины «тымак».
