                <p id='textMeat'>Иримшик – в отличие от курта, который есть и в других среднеазиатских странах, – только казахский. Это творог, который имеет слегка сладковатый вкус даже без добавления сахара. Чтобы приготовить иримшик, нужно терпение. Молоко доводят до кипения на медленном огне, затем немного охлаждают, добавляют закваску, перемешивают и оставляют на пару часов. Затем снова ставят молоко на медленный огонь и доводят до кипения. Когда образуется творожный сгусток, отделяют сыворотку. Затем кладут творог в мешочек и дают стечь лишней жидкости.

